
package Modelo;

/**
 *
 * @author NOTBOOK DE MAYCON
 */
public class ModeloTelefone {
   private int cod;
   private String tel;
   private String whats;

    /**
     * @return the cod
     */
    public int getCod() {
        return cod;
    }

    /**
     * @param cod the cod to set
     */
    public void setCod(int cod) {
        this.cod = cod;
    }

    /**
     * @return the tel
     */
    public String getTel() {
        return tel;
    }

    /**
     * @param tel the tel to set
     */
    public void setTel(String tel) {
        this.tel = tel;
    }

    /**
     * @return the whats
     */
    public String getWhats() {
        return whats;
    }

    /**
     * @param whats the whats to set
     */
    public void setWhats(String whats) {
        this.whats = whats;
    }
      
}
