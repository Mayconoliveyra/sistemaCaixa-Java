    private void tbl_cadastroFornMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbl_cadastroFornMousePressed
        modoNavegar = "Navegar";
        manipularInterface();

        String  codigo_forn = ""+ tbl_cadastroForn.getValueAt(tbl_cadastroForn.getSelectedRow(), 0);
        conecBanco.conexaoBanco();
        try {
            conecBanco.executaSQL("select * from fornecedores where id_fornecedor='"+codigo_forn+"'");
            conecBanco.rs.first();
            
            ct_ID.setText(String.valueOf(conecBanco.rs.getInt("id_fornecedor")));
            ct_nome.setText(conecBanco.rs.getString("nome_fornecedor"));
            ct_endereco.setText(conecBanco.rs.getString("endereco"));
            ctFor_cnpj.setText(conecBanco.rs.getString("cnpj_fornecedor"));
            ctFor_telefone1.setText(conecBanco.rs.getString("telefone_1"));
            ctFoR_telefone2.setText(conecBanco.rs.getString("telefone_2"));
            ct_nomefantasia.setText(conecBanco.rs.getString("nome_fantasia"));
            conecBanco.executaSQL("select * from bairro where id_bairro='"+conecBanco.rs.getInt("id_bairro")+"'");
            conecBanco.rs.first();
            cbox_bairro.setSelectedItem(conecBanco.rs.getString("nome_bairro"));

        } catch (SQLException ex) {

        }
        conecBanco.desconectar();
    }//GEN-LAST:event_tbl_cadastroFornMousePressed

