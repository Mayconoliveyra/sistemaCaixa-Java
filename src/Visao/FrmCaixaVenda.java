    private void btn_addItemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_addItemActionPerformed
        lb_quantidadeEstoque.setText("");
        ////    ABRIR VENDA/////
        if(opAbrirVenda==2){
            modoNavegar = "AbriVendaFV"; // RESETA FRAME FINALIZAR VENDA
            manipularInterface();

            conecBanco.conexaoBanco();  // METODO PARA ABRIR UMA VENDDA
            try {
                PreparedStatement pst = conecBanco.conn.prepareStatement("insert into venda (valor_venda) values (?)");
                pst.setFloat(1,0);
                pst.execute();

                conecBanco.executaSQL("select * from venda");
                conecBanco.rs.last();
                codVenda = conecBanco.rs.getInt("id_venda");
                //            JOptionPane.showMessageDialog(rootPane,codVenda);
                ct_codVenda.setText(String.valueOf(codVenda));
                ct_codVenda4.setText(String.valueOf(codVenda));
                ct_codVenda5.setText(String.valueOf(codVenda));
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(rootPane,"Erro ao Criar Venda"+ ex);
            }
            conecBanco.desconectar();
        }else{
        }

        //// ADICIONAR PRODUTO///
        try {
            int quantidade=0;
            int qtd;

            conecBanco.conexaoBanco();
            conecBanco.executaSQL("select * from produto where nome_produto='"+ct_nomeProduto.getText()+"'");
            conecBanco.rs.first();
            quantidade = conecBanco.rs.getInt("quantidade");
            conecBanco.desconectar();
            if(quantidade >= Integer.parseInt(ctFor_quantidade.getText())){
                mod.setNomeProduto(ct_nomeProduto.getText());
                mod.setQtdItem(Integer.parseInt(ctFor_quantidade.getText()));

                mod.setIdVenda(codVenda);
                control.adicionaItem(mod);
                somaProduto();
                preencheVenda();
                preecherTabelaItensVendaCaixa("select * from produto inner join itens_venda_produto on "
                    + "produto.id_produto = itens_venda_produto.id_produto inner join venda on "
                    + "venda.id_venda= itens_venda_produto.id_venda where venda.id_venda="+codVenda);

                preecherTabelaItensVendaCaixaFinalizar("select * from produto inner join itens_venda_produto on "
                    + "produto.id_produto = itens_venda_produto.id_produto inner join venda on "
                    + "venda.id_venda= itens_venda_produto.id_venda where venda.id_venda="+codVenda);

                modoNavegar = "AdicionarProduto";
                manipularInterface();

            }else{
                JOptionPane.showMessageDialog(rootPane, "PRODUTO INDISPONÍVEL!");
            }

        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao Pesquisar Quantidade do Produto!"+ex);
        }
        btn_addItem.setEnabled(false);
        ctFor_quantidade.setEnabled(false);
        opAbrirVenda=1; //SIGNIFICA QUE A VENDA JA ESTA ABERTA
    }//GEN-LAST:event_btn_addItemActionPerformed

