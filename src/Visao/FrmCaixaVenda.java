    private void ct_recebidoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_ct_recebidoKeyReleased

        float subCompra;
        if(ct_recebido.getText().equals("")){
            subCompra= (ValorTotalDACompra - ValorTotalDACompra- ValorTotalDACompra);
            ct_troco.setText(String.valueOf(numberFormat.format(subCompra)));
        }else{
            if(opcForPag==1){
                ct_recebido.setText(ct_recebido.getText());
                troco = Float.parseFloat(ct_recebido.getText()) - subTotal;
                ct_troco.setText(numberFormat.format(troco));
            }else{
                ct_recebido.setText(ct_recebido.getText());
                troco = Float.parseFloat(ct_recebido.getText()) - ValorTotalDACompra;
                ct_troco.setText(numberFormat.format(troco));
            }
            //IF BTN CONFIRMA VENDA
            if(subTotal<=Float.parseFloat(ct_recebido.getText())){
                btn_confirmaVenda.setEnabled(true);
            }else{
                btn_confirmaVenda.setEnabled(false);
            }

        }
    }//GEN-LAST:event_ct_recebidoKeyReleased

