    public void descontoM(){ //CALCULA O DESCONTO
    float desconto;
    float porc;
    
    porc = (float)4/100;
    desconto =  ValorTotalDACompra * porc;
    subTotal= ValorTotalDACompra- desconto;
    
    ct_valorTotalCompraFinaliza.setText(String.valueOf(numberFormat.format(ValorTotalDACompra)));
    ct_desconto.setText(String.valueOf(numberFormat.format(desconto)));
    ct_subtotal.setText(String.valueOf(numberFormat.format(subTotal)));

}
 public void preencheVenda(){ //  METODO PARA PREENCHER  CAIXA DE TEXTOS DA TABELA FINALIZARVENDA NA ABERTURA PRINCIPAL
     ct_recebido.setText(""); 
     ct_troco.setText("");
    ct_valorTotalCompraFinaliza.setText(String.valueOf(numberFormat.format(ValorTotalDACompra)));
    ct_subtotal.setText(String.valueOf(numberFormat.format(ValorTotalDACompra)));
    ct_recebido.setText(ct_recebido.getText());

 }
    
    
