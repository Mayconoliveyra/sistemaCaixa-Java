    private void btn_excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_excluirActionPerformed
      int respost=JOptionPane.showConfirmDialog(null,"Deseja excluir marca?","Excluir",JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE);
      if(respost==JOptionPane.YES_NO_OPTION){
        conecBan.conexaoBanco();
        try {
            PreparedStatement pst = conecBan.conn.prepareStatement("delete from marca where id_marca=?");
            pst.setInt(1,Integer.parseInt(ct_cod.getText()));
            pst.execute();
           JOptionPane.showMessageDialog(rootPane, "Marca excluida!");  
        } catch (SQLException ex) {
           JOptionPane.showMessageDialog(rootPane, "Erro ao excluir marca"+ ex); 
        }
       
       preecherTabela("select * from marca order by nome_marca");
       conecBan.desconectar();
        modoNavegar = "Inicializar";
        manipularInterface();
      }
    }//GEN-LAST:event_btn_excluirActionPerformed

