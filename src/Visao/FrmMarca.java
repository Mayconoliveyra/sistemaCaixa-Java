    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
          int op1, op2, op3;
         String verificMarca="";
         
        conecBan.conexaoBanco();
        try {
            conecBan.executaSQL("select * from marca where nome_marca='"+ct_nomeMarca.getText()+"'");
            conecBan.rs.first();
            verificMarca= conecBan.rs.getString("nome_marca");
        } catch (SQLException ex) {
        }
        conecBan.desconectar();
        
        if(ct_nomeMarca.getText().equals("")){
           ct_nomeMarca.setBackground(new Color(246,170,170));
            op1=1;
        }else{
            ct_nomeMarca.setBackground(new Color(255,255,102));
            op1=2;
        }
        
        if(ct_nomeMarca.getText().equals(verificMarca)){
            op2=1;
        }else{
            op2=2;
        }
        
       
       
       if(op1==2){
        conecBan.conexaoBanco();
        
        if(modoNavegar.equals("Novo")){
            if(op2==1){
             JOptionPane.showMessageDialog(null,"Nome da marca já cadastrado! \nTente outro nome.","Erro",JOptionPane.ERROR_MESSAGE);      
            }else{
                int resposta=  JOptionPane.showConfirmDialog(null, "Salvar nova marca?", "Novo",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
                if(resposta== JOptionPane.YES_NO_OPTION){  
                 try {
                     PreparedStatement pst = conecBan.conn.prepareStatement("insert into marca(nome_marca)values(?)");
                     pst.setString(1, ct_nomeMarca.getText());
                     pst.execute();
                     JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso!");  

                 } catch (SQLException ex) {
                   JOptionPane.showMessageDialog(rootPane, "Erro ao adicinar marca"+ ex);   
                 }
                modoNavegar="Inicializar";
                manipularInterface();
                }
            }
        }

        if(modoNavegar.equals("Editar")){
             int resposta=  JOptionPane.showConfirmDialog(null, "Deseja salvar as alterações?", "Alteração",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
             if(resposta== JOptionPane.YES_NO_OPTION){  
                 try {
                   PreparedStatement pst = conecBan.conn.prepareStatement("update marca set nome_marca=? where id_marca=?");
                      pst.setString(1, ct_nomeMarca.getText());
                      pst.setInt(2,Integer.parseInt(ct_cod.getText()));
                      pst.execute();
                      JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso!");  
                 } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(rootPane, "Erro ao editar marca"+ ex);  
                 }
                 modoNavegar="Navegar";
                 manipularInterface();
             }
        }
        preecherTabela("select * from marca order by nome_marca");
        conecBan.desconectar();
       }else{
       JOptionPane.showMessageDialog(null,"Campo obrigatório não preenchido!","Erro",JOptionPane.ERROR_MESSAGE);      
       }
    }//GEN-LAST:event_btn_salvarActionPerformed

