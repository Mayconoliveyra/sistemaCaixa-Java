    private void jButtonSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSalvarActionPerformed
        int op1, op2, op3;
        String nomeEstadoVerif="";
        conecBanco.conexaoBanco();
        try {
            conecBanco.executaSQL("select * from estados where nome_estado='"+jTextFieldNome.getText()+"'");
            conecBanco.rs.first();
            nomeEstadoVerif=conecBanco.rs.getString("nome_estado");
        } catch (SQLException ex) {
          
        }

        if(jTextFieldNome.getText().equals("")){
            jTextFieldNome.setBackground(new Color(246,170,170));
            op1=1;
        }else{
            jTextFieldNome.setBackground(new Color(255,255,102));
            op1=2;
        }
        
        if(jTextFieldSigla.getText().equals("")){
            jTextFieldSigla.setBackground(new Color(246,170,170));
            op2=1;
        }else{
            jTextFieldSigla.setBackground(new Color(255,255,102));
            op2=2;
        }
        
        if(jTextFieldNome.getText().equals(nomeEstadoVerif)){
            op3=1;
        }else{
            op3=2;
        }
        
        if(op1==2 && op2==2){
            if(modoNavegar.equals("Novo")){ // NOVO CADASTRO
                    if(op3==1){
                        JOptionPane.showMessageDialog(null,"Nome de estado já cadastrado! \nTente outro nome.","Erro",JOptionPane.ERROR_MESSAGE);
                    }else{
                        int resposta=  JOptionPane.showConfirmDialog(null, "Salvar novo estado?", "Novo",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
                        if(resposta== JOptionPane.YES_NO_OPTION){
                            try {
                                PreparedStatement pst = conecBanco.conn.prepareStatement("insert into estados(nome_estado, sigla_estado)values(?,?)");// PASSAGEM DO SQL PARA DE INSERÇÃO DE DADOS
                                pst.setString(1, jTextFieldNome.getText()); // PASSAGEM DOS PARÁMETROS
                                pst.setString(2, jTextFieldSigla.getText());
                                pst.executeUpdate(); // EXECUTA A INSERÇÃO
                                JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso!");
                                modoNavegar ="Inicializar";
                                manipularInterface();

                            } catch (SQLException ex) {
                                JOptionPane.showMessageDialog(rootPane, "Erro na inserção \n"+ ex);
                            }
                        }else{ 
                        }
                    }

            }
            
            if(modoNavegar.equals("Editar")){ // EDITAR CADASTRO
                    int resposta=  JOptionPane.showConfirmDialog(null, "Deseja salvar as alterações?", "Alteração",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
                    if(resposta== JOptionPane.YES_NO_OPTION){
                        try {
                            PreparedStatement pst= conecBanco.conn.prepareStatement("update estados set nome_estado= ?, sigla_estado=? where id_estado=?");
                            pst.setString(1, jTextFieldNome.getText());
                            pst.setString(2, jTextFieldSigla.getText());
                            pst.setInt(3, Integer.parseInt(jTextFieldCod.getText()));
                            pst.execute();
                            JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso!");
                            modoNavegar ="Navegar";
                            manipularInterface();
                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(rootPane, "Erro ao Alterar \n"+ ex.getMessage());
                        }
                    }
                }
            
            preecherTabela("select * from estados order by  id_estado"); // IMPRIMI TABELA TODA VEZ QUE CHAMA UM MODO DE NAVEGAÇÃO
        }else{
         JOptionPane.showMessageDialog(null,"Campo obrigatório não preenchido!","Erro",JOptionPane.ERROR_MESSAGE);    
        }
        conecBanco.desconectar();
    }//GEN-LAST:event_jButtonSalvarActionPerformed

