    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
       int respost=JOptionPane.showConfirmDialog(null,"Deseja excluir estado?","Excluir",JOptionPane.YES_OPTION, JOptionPane.QUESTION_MESSAGE);
       if(respost==JOptionPane.YES_NO_OPTION){
        conecBanco.conexaoBanco();
        try {
            PreparedStatement pst= conecBanco.conn.prepareStatement("delete from estados where id_estado=?");
            pst.setInt(1, Integer.parseInt(jTextFieldCod.getText()));
            pst.execute();
            JOptionPane.showMessageDialog(rootPane, "Excluido com sucesso!");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(rootPane, "Erro ao Excluir \n"+ ex);
        }
        preecherTabela("select * from estados order by  id_estado"); // IMPRIMI TABELA TODA VEZ QUE CHAMA UM MODO DE NAVEGAÇÃO
        conecBanco.desconectar();

        modoNavegar = "Inicializar";
        manipularInterface();
       }else{
           
       }
    }//GEN-LAST:event_jButtonDeletarActionPerformed

