    public void preecherTabela(String SQL){ // METODO PARA PREENCHER TABELA NA INTERFACE
        ArrayList dados = new ArrayList();
        
        String [] Colunas  = new String []{"Nome", "Apelido", "Telefone I","CPF","RG", "Bairro"};
        conecBanco.conexaoBanco();
        conecBanco.executaSQL(SQL);
      try {
          conecBanco.rs.first();
          do {
              conecBancoBairro.conexaoBanco();
              conecBancoBairro.executaSQL("select * from bairro where id_bairro="+conecBanco.rs.getString("id_bairro")+"");
              conecBancoBairro.rs.first();
              conecBancoBairro.desconectar();
             
              dados.add(new Object[]{conecBanco.rs.getString("nome_cliente"),conecBanco.rs.getString("apelido_cliente"),conecBanco.rs.getString("telefone_1"),conecBanco.rs.getString("cpf_cliente"),conecBanco.rs.getString("rg_cliente"),conecBancoBairro.rs.getString("nome_bairro")});
          }while (conecBanco.rs.next());
          
      } catch (SQLException ex) {
//         JOptionPane.showMessageDialog(rootPane, "Erro ao Preencher o ArrayList! \n"+ ex);
      }
      
       ModeloTabela modelo = new ModeloTabela(dados,Colunas);
       
        tbl_clientes.setModel(modelo);
        DefaultTableCellRenderer headerRenderer2 = new DefaultTableCellRenderer(); 
        headerRenderer2.setOpaque(true); 
        headerRenderer2.setBackground(new Color(0,0,51));
        headerRenderer2.setFont(new java.awt.Font("Arial Black", Font.BOLD, 23));
        headerRenderer2.setForeground(new Color(255,255,255));
        for (int i = 0; i < tbl_clientes.getModel().getColumnCount(); 
         i++) { tbl_clientes.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer2); }

            tbl_clientes.setRowHeight(31); //  TAMANHO DA JANELA PRA CADA ITEM
            
        tbl_clientes.getColumnModel().getColumn(0).setPreferredWidth(240);
        tbl_clientes.getColumnModel().getColumn(0).setResizable(false);
        tbl_clientes.getColumnModel().getColumn(1).setPreferredWidth(150);
        tbl_clientes.getColumnModel().getColumn(1).setResizable(false);
        tbl_clientes.getColumnModel().getColumn(2).setPreferredWidth(110);
        tbl_clientes.getColumnModel().getColumn(2).setResizable(false);
        tbl_clientes.getColumnModel().getColumn(3).setPreferredWidth(110);
        tbl_clientes.getColumnModel().getColumn(3).setResizable(false);
        tbl_clientes.getColumnModel().getColumn(4).setPreferredWidth(110);
        tbl_clientes.getColumnModel().getColumn(4).setResizable(false);
        tbl_clientes.getColumnModel().getColumn(5).setPreferredWidth(150);
        tbl_clientes.getColumnModel().getColumn(5).setResizable(false);
        tbl_clientes.getTableHeader().setReorderingAllowed(false);
//        tbl_clientes.setAutoResizeMode(tbl_clientes.AUTO_RESIZE_OFF);
        tbl_clientes.setSelectionMode(ListSelectionModel.SINGLE_SELECTION);
        conecBanco.desconectar();
    }//  FINAL METODO PARA IMPRIMIR TABELA NA INTERFACE
                    
