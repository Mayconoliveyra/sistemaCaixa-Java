    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
      int op1, op2, op3;
      String verificaNome = "";
      conecBanco.conexaoBanco();
            try {
                conecBanco.executaSQL("select * from clientes where nome_cliente='"+ct_nome.getText()+"'");
                conecBanco.rs.first();
                verificaNome= conecBanco.rs.getString("nome_cliente");
            } catch (SQLException ex) {
            }
      
      conecBanco.desconectar();
        if(ct_nome.getText().equals("")){
            ct_nome.setBackground(new Color(246,170,170));
            op1=1;        
        }else{
            ct_nome.setBackground(new Color(255,255,102));
            op1=2; 
        }

         if(cbox_bairro.getSelectedIndex()==-1){
            cbox_bairro.setBorder(new LineBorder(new Color(255,0,0)));
            op2=1;        
        }else{
            cbox_bairro.setBorder(new LineBorder(null));
            op2=2; 
        }
        
        if(ct_nome.getText().equals(verificaNome)){
            op3=1;
        }else{
            op3=2;
        }
        
        if(op1==2 && op2==2){
                if(modoNavegar.equals("Novo")){
                    if(op3==1){
                       JOptionPane.showMessageDialog(null,"Nome de cliente já cadastrado! \nTente outro nome.","Erro",JOptionPane.ERROR_MESSAGE);    
                    }else{
                       int resposta=  JOptionPane.showConfirmDialog(null, "Salvar novo cliente?", "Novo",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
                       if(resposta== JOptionPane.YES_NO_OPTION){
                        mod.setNome(ct_nome.getText());
                        mod.setTelefone1(ct_telefone1.getText().replaceAll("[^0-9]", ""));
                        mod.setTelefone2(ct_telefone2.getText().replaceAll("[^0-9]", ""));
                        mod.setEndereco(ct_endereco.getText());
                        mod.setRg(ct_rg.getText().replaceAll("[^0-9]", ""));
                        mod.setCpf(ct_cpf.getText().replaceAll("[^0-9]", ""));
                        mod.setBairro(""+cbox_bairro.getSelectedItem());
                        mod.setApelido(ct_apelido.getText());
                        mod.setReferencia(ct_referencia.getText());
                        contrCli.Gravar(mod);
                        modoNavegar = "Inicializar";
                        manipularInterface();
                       }
                    }
                }

                if(modoNavegar.equals("Editar")){
                   int resposta=  JOptionPane.showConfirmDialog(null, "Deseja salvar as alterações?", "Alteração",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
                   if(resposta== JOptionPane.YES_NO_OPTION){
                    mod.setId(Integer.parseInt(ct_codigo.getText()));
                    mod.setNome(ct_nome.getText());
                    mod.setTelefone1(ct_telefone1.getText().replaceAll("[^0-9]", ""));
                    mod.setTelefone2(ct_telefone2.getText().replaceAll("[^0-9]", ""));
                    mod.setEndereco(ct_endereco.getText());
                    mod.setRg(ct_rg.getText().replaceAll("[^0-9]", ""));
                    mod.setCpf(ct_cpf.getText().replaceAll("[^0-9]", ""));
                    mod.setBairro("" + cbox_bairro.getSelectedItem());
                    mod.setApelido(ct_apelido.getText());
                    mod.setReferencia(ct_referencia.getText());
                    contrCli.Editar(mod);
                    modoNavegar = "Navegar";
                    manipularInterface();
                   }
                }
                preecherTabela("select * from clientes  order by nome_cliente");
        }else{
         JOptionPane.showMessageDialog(null,"Campo obrigatório não preenchido!","Erro",JOptionPane.ERROR_MESSAGE);    
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

