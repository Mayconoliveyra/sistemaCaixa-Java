    private void btn_salvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_salvarActionPerformed
         int op1, op2, op3;
         String verificGrupo="";
         
        conecBan.conexaoBanco();
        try {
            conecBan.executaSQL("select * from grupo_produto where nome_grupo='"+ct_nomeGrupo.getText()+"'");
            conecBan.rs.first();
            verificGrupo= conecBan.rs.getString("nome_grupo");
        } catch (SQLException ex) {
        }
        conecBan.desconectar();
        
    
        
        if(ct_nomeGrupo.getText().equals("")){
           ct_nomeGrupo.setBackground(new Color(246,170,170));
            op1=1;
        }else{
            ct_nomeGrupo.setBackground(new Color(255,255,102));
            op1=2;
        }
        
        if(ct_nomeGrupo.getText().equals(verificGrupo)){
            op2=1;
        }else{
            op2=2;
        }
        
        if(op1==2){
            conecBan.conexaoBanco();
            if(modoNavegar.equals("Novo")){
                if(op2==1){
                 JOptionPane.showMessageDialog(null,"Nome do grupo j√° cadastrado! \nTente outro nome.","Erro",JOptionPane.ERROR_MESSAGE);   
                }else{
                    int resposta=  JOptionPane.showConfirmDialog(null, "Salvar novo grupo?", "Novo",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
                    if(resposta== JOptionPane.YES_NO_OPTION){
                        try {
                            PreparedStatement pst = conecBan.conn.prepareStatement("insert into grupo_produto(nome_grupo)values(?)");
                            pst.setString(1, ct_nomeGrupo.getText());
                            pst.execute();
                            JOptionPane.showMessageDialog(rootPane, "Salvo com sucesso!");

                        } catch (SQLException ex) {
                            JOptionPane.showMessageDialog(rootPane, "Erro ao adicionar grupo"+ ex);
                        }
                     modoNavegar="Inicializar";
                     manipularInterface();
                    }
                }
            }

            if(modoNavegar.equals("Editar")){
                int resposta=  JOptionPane.showConfirmDialog(null, "Deseja salvar as altera√ß√µes?", "Altera√ß√£o",JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);    
                if(resposta== JOptionPane.YES_NO_OPTION){  
                    try {
                        PreparedStatement pst = conecBan.conn.prepareStatement("update grupo_produto set nome_grupo=? where id_grupo=?");
                        pst.setString(1, ct_nomeGrupo.getText());
                        pst.setInt(2,Integer.parseInt(ct_cod.getText()));
                        pst.execute();
                        JOptionPane.showMessageDialog(rootPane, "Alterado com sucesso!");
                    } catch (SQLException ex) {
                        JOptionPane.showMessageDialog(rootPane, "Erro ao editar grupo"+ ex);
                    }
                    modoNavegar="Navegar";
                    manipularInterface();
                }
            }
            
            preecherTabela("select * from grupo_produto order by nome_grupo");
            conecBan.desconectar();
        }else{
         JOptionPane.showMessageDialog(null,"Campo obrigat√≥rio n√£o preenchido!","Erro",JOptionPane.ERROR_MESSAGE);    
        }
    }//GEN-LAST:event_btn_salvarActionPerformed

